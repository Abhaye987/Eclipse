package operators;

public class arthematicop {
	public static void main(String args[]) {
		int num1=20,num2=10;
		int sum,sub,mul,div,mod;
		sum=num1+num2;
		sub=num1-num2;
		mul=num1*num2;
		div=num1/num2;
		mod=num1%num2;
		
		System.out.println("sum: "+sum +"\nsub:"+sub+"\nmul: "+mul+"\ndiv:"+div+"\n mod :"+mod);
	}

}
